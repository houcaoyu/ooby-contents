angular.module('core.author', ['ngResource']).
  service('Author', function($resource) {
    return $resource('/author/:id',{},{
      hotest:{
        method:'GET',
        isArray:true,
        params:{number:6},
        url:'/contentgroup/hotest'
      },
      hotestOfCategory:{
        method:'GET',
        isArray:true,
        params:{number:6},
        url:'/contentgroup/hotest/ofcategory'
      },
      ofAuthor:{
        method:'GET',
        isArray:true,
        url:'/contentgroup/ofauthor/:authorId'
      },
      ofTag:{
        method:'GET',
        isArray:true,
        url:'/contentgroup/oftag/:tagId'
      },
      ofSubscriber:{
        method:'GET',
        isArray:true,
        url:'/contentgroup/ofsubscriber/:subscriberId'
      },
      update:{
        method:'PUT'
      },
      subscribe:{
        method:'POST',
        url:'/contentgroup/subscribe'
      },
      unsubscribe:{
        method:'POST',
        url:'/contentgroup/unsubscribe'
      }
    })
  })
